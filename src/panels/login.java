    private void jPanel12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel12MouseClicked
        // TODO add your handling code here:
        
            ConnectionEasy obj = new ConnectionEasy();
            String sql = "Select * From Events";
            ResultSet rs = obj.Select(sql);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
              try {
                while(rs.next())
                {
                model.addRow(new Object[] {rs.getString("EventId"),rs.getString("EventName"),rs.getString("Location"),rs.getString("StartTime"),rs.getString("EndTime"),rs.getString("Date"),rs.getString("Price")});
                }
                
            
            if(!rs.first())
            {
                int i = model.getRowCount();
                while(i > 0)
                {
                    model.removeRow(0);
                    i--;
                }
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jPanel12MouseClicked

