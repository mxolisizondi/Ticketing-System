   public String getID(String id)
   {
       if(id.length() != 13) return null;
       for(int i = 0;i < 13;i++)
       {
          if(id.charAt(i) != 0||id.charAt(i) != 1 ||id.charAt(i) != 2||id.charAt(i) != 3||id.charAt(i) != 4||id.charAt(i) != 5||id.charAt(i) != 6||id.charAt(i) != 7||id.charAt(i) != 8||id.charAt(i) != 9)
         {
           return null;
         } 
       }
       return id;
   }
